import { appendFile, mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import type { Command } from "commander";
import {
  parseNonNegativeIntegerOption,
  parsePositiveIntegerOption,
  readSophiaFilesFromDomains,
  setFailedExitIf,
} from "./cli_utils.js";
import { runDirectTsExperiment } from "../experiment/direct_ts_runner.js";
import { runFullExperiment } from "../experiment/full_runner.js";
import { loadBenchmarkSuite, loadBenchmarkTask } from "../experiment/task.js";
import { verifySophiaFilesAgainstTask } from "../experiment/verify.js";

export function registerExperimentCommands(program: Command): void {
  const experiment = program
    .command("experiment")
    .description("Run benchmark experiments without leaking hidden verifier cases to the model");

  experiment
    .command("list")
    .requiredOption("--suite <path>", "Benchmark suite directory or task.json")
    .description("List benchmark tasks")
    .action(async (options: { suite: string }) => {
      const tasks = await loadBenchmarkSuite(options.suite);
      console.log(
        JSON.stringify(
          tasks.map((task) => ({
            id: task.id,
            title: task.title,
            category: task.category,
            action: task.scaffold.action,
            hidden_cases: task.hidden_cases.length,
          })),
          null,
          2,
        ),
      );
    });

  experiment
    .command("verify")
    .requiredOption("--task <path>", "Benchmark task.json")
    .description("Verify currently materialized domains against a hidden benchmark task")
    .action(async (options: { task: string }) => {
      const task = await loadBenchmarkTask(options.task);
      const files = await readSophiaFilesFromDomains(process.cwd());
      const result = await verifySophiaFilesAgainstTask(files, task);
      console.log(JSON.stringify(result, null, 2));
      setFailedExitIf(!result.ok);
    });

  experiment
    .command("run")
    .requiredOption("--task <path>", "Single benchmark task.json")
    .requiredOption("--model <model>", "Ollama model name")
    .option("--mode <mode>", "Experiment mode: full or direct-ts", "full")
    .option("--max-design-revisions <count>", "Design revision budget", "2")
    .option("--max-repairs <count>", "Code repair budget", "2")
    .option("--ollama-timeout-ms <count>", "Timeout for each Ollama generate call")
    .option("--ollama-num-predict <count>", "Maximum tokens generated by each Ollama call")
    .option("--runs <count>", "Number of independent runs to execute", "1")
    .option("--out <path>", "Optional JSONL output path")
    .option("--append", "Append to an existing JSONL output instead of replacing it")
    .description("Run one leakage-safe benchmark experiment for one task")
    .action(
      async (options: {
        task: string;
        model: string;
        mode: string;
        maxDesignRevisions: string;
        maxRepairs: string;
        ollamaTimeoutMs?: string;
        ollamaNumPredict?: string;
        runs: string;
        out?: string;
        append?: boolean;
      }) => {
        if (options.mode !== "full" && options.mode !== "direct-ts") {
          throw new Error("--mode must be full or direct-ts.");
        }
        const maxDesignRevisions = parseNonNegativeIntegerOption(
          options.maxDesignRevisions,
          "--max-design-revisions",
        );
        const maxRepairs = parseNonNegativeIntegerOption(options.maxRepairs, "--max-repairs");
        applyOllamaOptions(options);
        const task = await loadBenchmarkTask(options.task);
        const runs = parsePositiveIntegerOption(options.runs, "--runs");
        if (options.out && !options.append) {
          await mkdir(path.dirname(options.out), { recursive: true });
          await writeFile(options.out, "", "utf8");
        }
        let anyFailed = false;
        for (let runIndex = 0; runIndex < runs; runIndex += 1) {
          const record = await runBenchmarkRecord({
            task,
            model: options.model,
            mode: options.mode,
            maxDesignRevisions,
            maxRepairs,
            runIndex,
            runsTotal: runs,
          });
          console.log(runs === 1 ? JSON.stringify(record, null, 2) : JSON.stringify(record));
          if (options.out) {
            await mkdir(path.dirname(options.out), { recursive: true });
            await appendFile(options.out, `${JSON.stringify(record)}\n`, "utf8");
          }
          if (!record.ok) anyFailed = true;
        }
        setFailedExitIf(anyFailed);
      },
    );

  experiment
    .command("run-suite")
    .option("--suite <path>", "Benchmark suite directory or task.json", "benchmarks")
    .requiredOption("--model <model>", "Ollama model name")
    .option("--mode <mode>", "Experiment mode: full or direct-ts", "full")
    .option("--max-design-revisions <count>", "Design revision budget", "2")
    .option("--max-repairs <count>", "Code repair budget", "2")
    .option("--ollama-timeout-ms <count>", "Timeout for each Ollama generate call")
    .option("--ollama-num-predict <count>", "Maximum tokens generated by each Ollama call")
    .option("--runs <count>", "Number of independent runs per task", "1")
    .option("--out-dir <path>", "Directory for suite JSONL and summary outputs")
    .option("--append", "Append to an existing suite JSONL output instead of replacing it")
    .description("Run benchmark tasks serially, using the same single-task experiment path")
    .action(
      async (options: {
        suite: string;
        model: string;
        mode: string;
        maxDesignRevisions: string;
        maxRepairs: string;
        ollamaTimeoutMs?: string;
        ollamaNumPredict?: string;
        runs: string;
        outDir?: string;
        append?: boolean;
      }) => {
        if (options.mode !== "full" && options.mode !== "direct-ts") {
          throw new Error("--mode must be full or direct-ts.");
        }
        const maxDesignRevisions = parseNonNegativeIntegerOption(
          options.maxDesignRevisions,
          "--max-design-revisions",
        );
        const maxRepairs = parseNonNegativeIntegerOption(options.maxRepairs, "--max-repairs");
        const runs = parsePositiveIntegerOption(options.runs, "--runs");
        applyOllamaOptions(options);

        const tasks = await loadBenchmarkSuite(options.suite);
        const outDir =
          options.outDir ??
          path.join(
            "sophia-runs",
            "results",
            `benchmark-suite-${new Date().toISOString().replace(/[:.]/g, "-")}`,
          );
        await mkdir(outDir, { recursive: true });
        const resultsPath = path.join(outDir, "results.jsonl");
        const summaryPath = path.join(outDir, "summary.md");
        if (!options.append) {
          await writeFile(resultsPath, "", "utf8");
        }
        const records: ExperimentRecord[] = [];
        let anyFailed = false;

        for (const task of tasks) {
          for (let runIndex = 0; runIndex < runs; runIndex += 1) {
            const record = await runBenchmarkRecord({
              task,
              model: options.model,
              mode: options.mode,
              maxDesignRevisions,
              maxRepairs,
              runIndex,
              runsTotal: runs,
            });
            records.push(record);
            await appendFile(resultsPath, `${JSON.stringify(record)}\n`, "utf8");
            const status = record.ok === true ? "ok" : "fail";
            console.log(
              JSON.stringify({
                status,
                task: task.id,
                mode: options.mode,
                run_index: runIndex,
                runs_total: runs,
                results: resultsPath,
              }),
            );
            if (!record.ok) anyFailed = true;
          }
        }

        const summary = summarizeExperimentRecords(records);
        await writeFile(summaryPath, `${summary}\n`, "utf8");
        console.log(
          JSON.stringify({ ok: !anyFailed, tasks: tasks.length, resultsPath, summaryPath }),
        );
        setFailedExitIf(anyFailed);
      },
    );

  experiment
    .command("summarize")
    .requiredOption("--inputs <paths...>", "JSONL result files from experiment run --out")
    .description("Summarize benchmark experiment JSONL results")
    .action(async (options: { inputs: string[] }) => {
      const records = (await Promise.all(options.inputs.map(readJsonlRecords))).flat();
      console.log(summarizeExperimentRecords(records));
      setFailedExitIf(records.length === 0);
    });
}

interface ExperimentRecord {
  ok?: unknown;
  task_id?: unknown;
  mode?: unknown;
  repairs_used?: unknown;
  design_revisions_used?: unknown;
  wall_time_ms?: unknown;
  run_index?: unknown;
  runs_total?: unknown;
}

interface RunBenchmarkRecordOptions {
  task: Awaited<ReturnType<typeof loadBenchmarkTask>>;
  model: string;
  mode: string;
  maxDesignRevisions: number;
  maxRepairs: number;
  runIndex: number;
  runsTotal: number;
}

async function runBenchmarkRecord(options: RunBenchmarkRecordOptions): Promise<ExperimentRecord> {
  const startedAt = Date.now();
  const result =
    options.mode === "full"
      ? await runFullExperiment({
          task: options.task,
          model: options.model,
          maxDesignRevisions: options.maxDesignRevisions,
          maxRepairs: options.maxRepairs,
        })
      : await runDirectTsExperiment({
          task: options.task,
          model: options.model,
        });
  return {
    ...result,
    run_index: options.runIndex,
    runs_total: options.runsTotal,
    wall_time_ms: Date.now() - startedAt,
  };
}

function applyOllamaOptions(options: {
  ollamaTimeoutMs?: string;
  ollamaNumPredict?: string;
}): void {
  if (options.ollamaTimeoutMs) {
    const timeoutMs = parsePositiveIntegerOption(options.ollamaTimeoutMs, "--ollama-timeout-ms");
    process.env.SOPHIA_OLLAMA_TIMEOUT_MS = String(timeoutMs);
  }
  if (options.ollamaNumPredict) {
    const numPredict = parsePositiveIntegerOption(options.ollamaNumPredict, "--ollama-num-predict");
    process.env.SOPHIA_OLLAMA_NUM_PREDICT = String(numPredict);
  }
}

async function readJsonlRecords(filePath: string): Promise<ExperimentRecord[]> {
  const content = await readFile(filePath, "utf8");
  return content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => JSON.parse(line) as ExperimentRecord);
}

function summarizeExperimentRecords(records: ExperimentRecord[]): string {
  const groups = new Map<string, ExperimentRecord[]>();
  for (const record of records) {
    const taskId = typeof record.task_id === "string" ? record.task_id : "<unknown>";
    const mode = typeof record.mode === "string" ? record.mode : "<unknown>";
    const key = `${taskId}\t${mode}`;
    groups.set(key, [...(groups.get(key) ?? []), record]);
  }

  const rows = [...groups.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([key, items]) => {
      const [taskId, mode] = key.split("\t");
      const successes = items.filter((item) => item.ok === true).length;
      return [
        taskId ?? "<unknown>",
        mode ?? "<unknown>",
        String(items.length),
        String(successes),
        formatRate(successes, items.length),
        formatAverage(items.map((item) => item.repairs_used)),
        formatAverage(items.map((item) => item.design_revisions_used)),
        formatAverage(items.map((item) => item.wall_time_ms)),
      ];
    });

  return [
    "| task | mode | runs | ok | success_rate | avg_repairs | avg_design_revisions | avg_wall_time_ms |",
    "|---|---|---:|---:|---:|---:|---:|---:|",
    ...rows.map((row) => `| ${row.join(" | ")} |`),
  ].join("\n");
}

function formatRate(successes: number, total: number): string {
  if (total === 0) return "0.000";
  return (successes / total).toFixed(3);
}

function formatAverage(values: unknown[]): string {
  const numbers = values.filter((value): value is number => typeof value === "number");
  if (numbers.length === 0) return "n/a";
  return (numbers.reduce((sum, value) => sum + value, 0) / numbers.length).toFixed(1);
}
